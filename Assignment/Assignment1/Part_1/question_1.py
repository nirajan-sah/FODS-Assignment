''' 
This program will calculate the perimeter of a rectangle with length 9 and breadth 6

The formula to calculate the perimeter of a rectangle is given by formula: 2*(length + breadth)
'''

length = 9
breadth = 6

#Calculating perimeter using formula
perimeter = 2 * (length + breadth)

#Printing the result
print(f"The perimeter of a rectangle with length {length} & {breadth} is {perimeter}")